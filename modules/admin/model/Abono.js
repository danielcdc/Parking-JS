class Abono {

    constructor(id_abono, pin, dni, fecha_inicio, fecha_fin, precio, id_plaza) {
        this.id_abono = id_abono;
        this.pin = pin;
        this.dni = dni;
        this.fecha_inicio = fecha_inicio;
        this.fecha_fin = fecha_fin;
        this.precio = precio;
        this.id_plaza = id_plaza;
    }
}